package com.playdata.attendanceSalary.atdSalDto.sal;

import com.playdata.attendanceSalary.atdSalEntity.sal.SalaryEntity;
import lombok.Data;

@Data
public class SalaryResponseDTO {
    private Long salaryId;
    private char salaryType;
    private String description;
    private String companyCode;

    public SalaryEntity toEntity() {
        SalaryEntity salaryEntity = new SalaryEntity();
        salaryEntity.setSalaryId(salaryId);
        salaryEntity.setSalaryType(salaryType);
        salaryEntity.setDescription(description);
        salaryEntity.setCompanyCode(companyCode);
        return salaryEntity;

    }

}
